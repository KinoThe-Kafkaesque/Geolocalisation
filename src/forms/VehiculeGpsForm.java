    private void loadTrackers() {
        for (GPSTracker t : ts.findAll()) {
            trackerList.addItem(t);
        }
    }

    private void loadVehicule() {
        for (Vehicule v : vs.findAll()) {
            vehiculesList.addItem(v);
        }
    }

    private void loadVg() {
        model.setRowCount(0);
        for (VehiculeGPSTracker vg : vgs.findAll()) {
            model.addRow(new Object[]{
                vg.getId(),
                vg.getVehicule(),
                vg.getGpsTracker(),
                vg.getDateDebut(),
                vg.getDateFin()
            });

        }
    }

