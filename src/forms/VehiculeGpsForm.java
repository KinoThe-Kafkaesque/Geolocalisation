    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int reponse = JOptionPane.showConfirmDialog(this, "Voulez vous vraiment dissocier cette vehicule ?");
        if (reponse == 0 && id != 0) {
            System.out.println(id);
            if (vgs.fin(vgs.findById(id))) {
                JOptionPane.showMessageDialog(this, "vehicule disscoier");
                loadVg();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        GPSTracker gps = (GPSTracker) trackerList.getSelectedItem();
        Vehicule vehicule = (Vehicule) vehiculesList.getSelectedItem();
        for (VehiculeGPSTracker v : vgs.findAll())
        {
            if(((v.getGpsTracker().getId() == gps.getId()) || (v.getVehicule().getId() == vehicule.getId())) &  (v.getDateFin() == null)){
             int reponse = JOptionPane.showConfirmDialog(this, "  gps ou  véhicule  déja lié,  voulez vous vraiment procéder  ?");
            if (!(reponse == 0)){ return;}
        }
        }
        
            
        if (vgs.create(new VehiculeGPSTracker(new Date(), vehicule, gps))) {
            JOptionPane.showMessageDialog(this, "laison établit");
            loadVg();
                    }
                      
    }//GEN-LAST:event_jButton1ActionPerformed

