    private void bnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnDeleteActionPerformed
        // TODO add your handling code here:
        int reponse = JOptionPane.showConfirmDialog(this, "Voulez vous vraiment supprimer ce tracker");
        if (reponse == 0 && id != 0) {
            if (vs.delete(vs.findById(id))) {
                JOptionPane.showMessageDialog(this, "Vehicule supprimé");
                loadVehicules();
            } else {
                JOptionPane.showMessageDialog(this, "suppression impossible");

            }
        }
    }//GEN-LAST:event_bnDeleteActionPerformed

    private void bnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnEditActionPerformed
        // TODO add your handling code here:
        Vehicule v = vs.findById(id);
        v.setMarque(marqueTxt.getText());
        v.setMatricule(matriculeTxt.getText());
        v.setType(typeTxt.getText());
        int reponse = JOptionPane.showConfirmDialog(this, "Voulez vous vraiment modifier cette voiture");

        if (reponse == 0 && id != 0) {
            if (vs.update(v)) {
                JOptionPane.showMessageDialog(this, "Tracker modifié");
                loadVehicules();
            }
        }
    }//GEN-LAST:event_bnEditActionPerformed

