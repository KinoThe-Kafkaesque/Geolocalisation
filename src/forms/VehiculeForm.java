    private void loadVehicules() {
        model.setRowCount(0);
        for (Vehicule v : vs.findAll()) {
            model.addRow(new Object[]{
                v.getId(),
                v.getMarque(),
                v.getMatricule(),
                v.getType(),
                v.getVehiculeGPSTracker()
            });

        }
    }

